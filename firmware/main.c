int main(int argc, char *argv[])
{
   int i = 0;
   
   hd44780_reset(0x2c);
   hd44780_ir_write(0x0f);
   hd44780_wait_busy();
   hd44780_ir_write(0x06);
   hd44780_wait_busy();
   for( i = 0; i < 80; i++) {
      hd44780_dr_write((' '+i) % 127);
      hd44780_wait_busy();
   }
   return 0;
}
