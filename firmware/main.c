#include <string.h>
#include <stdint.h>

#include <avr/eeprom.h>
#include <avr/interrupt.h>

#include "config.h"
#include "protocol.h"
#include "hd44780.h"
#include "uart.h"

/****************************************************************************
 * Definitions
 ****************************************************************************/

/****************************************************************************
 * Private Data
 ****************************************************************************/

/****************************************************************************
 * Private Functions
 ****************************************************************************/

/****************************************************************************
 * Public Functions
 ****************************************************************************/

int main(int argc, char *argv[])
{
   packet_t *packet = NULL;

   uart_init();

   hd44780_reset(0x2c);
   hd44780_ir_write(0x0f);
   hd44780_wait_busy();
   hd44780_ir_write(0x06);
   hd44780_wait_busy();

   sei();   
   while(1) {
      packet = uart_recv();

      hd44780_dr_write(packet->arg0 & 0x7f);
      hd44780_wait_busy();
   }
   return 0;
}
